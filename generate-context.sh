    fi
    
    # Informaci√≥n del sistema
    local system_info=$(uname -s)
    local node_version=$(node --version 2>/dev/null || echo "No Node.js")
    local npm_version=$(npm --version 2>/dev/null || echo "No npm")
    
    # Guardar informaci√≥n
    cat > /tmp/project_info.json << EOF
